package domain

import (
	"math"
	"testing"
)

// Справжні курси НБУ. Числа тут не вигадані: це те, що віддає
// /statdirectory/exchange на відповідні дати, і саме на них будувалось
// рішення міряти знецінення довгим вікном.
func TestAnnualPctOnRealRates(t *testing.T) {
	cases := []struct {
		name           string
		oldR, newR     float64
		days           int
		want, tolerate float64
	}{
		// Десятирічне вікно — те, за яким рахує застосунок. Дає майже ту
		// саму шістку, яку раніше підбирали головою.
		{"2016 → 2026, десять років", 24.000667, 44.811, 3854, 6.1, 0.15},
		// Річне вікно на тих самих даних дає майже нуль: гривня рік
		// стояла. Узяти його означало б оголосити гривневий папір під 16%
		// шістнадцятивідсотковим у реальному вимірі.
		{"2025 → 2026, один рік", 41.9725, 42.1701, 365, 0.5, 0.1},
		// А ось сусіднє однорічне вікно, що впіймало стрибок.
		{"2022 → 2023, один рік", 27.2782, 36.5686, 365, 34.1, 0.5},
	}
	for _, c := range cases {
		got, ok := AnnualPct(c.oldR, c.newR, c.days)
		if !ok {
			t.Errorf("%s: не порахувалось", c.name)
			continue
		}
		if math.Abs(got-c.want) > c.tolerate {
			t.Errorf("%s: маємо %.2f%%, очікували ≈%.1f%%", c.name, got, c.want)
		}
	}
}

// Складний відсоток, а не ділення на роки: інакше довгі вікна завищували б
// темп. Подвоєння за 10 років — це 7.18%/рік, а не 10%.
func TestAnnualPctCompounds(t *testing.T) {
	got, ok := AnnualPct(10, 20, 3653)
	if !ok {
		t.Fatal("не порахувалось")
	}
	if math.Abs(got-7.18) > 0.05 {
		t.Errorf("подвоєння за 10 років має дати ≈7.18%%/рік, маємо %.2f%%", got)
	}
}

// На коротких проміжках ануалізація перетворює добове коливання на сотні
// відсотків, тож числа краще не давати взагалі.
func TestAnnualPctRefusesShortWindows(t *testing.T) {
	if _, ok := AnnualPct(41.0, 41.5, 3); ok {
		t.Error("три дні мали дати ok=false")
	}
	if _, ok := AnnualPct(0, 44.8, 3650); ok {
		t.Error("нульовий курс мав дати ok=false")
	}
}
