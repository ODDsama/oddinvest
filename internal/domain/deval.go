package domain

import "math"

// Знецінення гривні — не константа з голови, а вимірювана величина: курс
// НБУ є за кожен день, і річний темп із нього рахується прямо.
//
// Вікно вимірювання має значення більше, ніж сама формула. Гривня падає
// стрибками, а не рівно, тож коротке вікно ловить або стрибок, або
// затишшя між ними, і число стає лотереєю: 2016→2020 дають −0.3%/рік,
// 2022→2023 дають +34%, 2025→2026 дають +0.5%. Десятирічне вікно
// усереднює і те, і те: станом на липень 2026 воно дає 6.1%/рік, і навіть
// раптовий стрибок курсу на 30% зрушив би його лише до ~8.7%.

// AnnualPct — річний темп зміни курсу між двома точками, у відсотках.
// Складний відсоток, а не ділення на роки: знецінення накопичується само
// на себе так само, як дохідність.
//
// ok=false, коли рахувати нема з чого: недодатні курси або менше ніж
// місяць між точками (на такому проміжку ануалізація дає сотні відсотків
// із добового коливання).
func AnnualPct(oldRate, newRate float64, days int) (float64, bool) {
	if oldRate <= 0 || newRate <= 0 || days < 30 {
		return 0, false
	}
	years := float64(days) / 365.25
	return (math.Pow(newRate/oldRate, 1/years) - 1) * 100, true
}
