// Дрібні шматки розмітки, спільні для розділів.
//
// Тут живе тільки те, що не залежить ані від даних, ані від контексту, —
// чиста функція «значення -> HTML». Щойно шматку знадобився store чи
// summary, він уже не компонент, а частина розділу.

import { esc, pct } from "./format.js";

export function tile(l, v, extra = "") {
  return `<div class="tile"><div class="lbl">${l}</div><div class="val">${v}</div>${extra}</div>`;
}

// У застосунку рівно два слова про дохідність, і жодне число не
// показується без одного з них.
//
// РЕАЛЬНА — скільки з приросту робить багатшим, після поправки на
// знецінення гривні. Це факт плюс припущення, і саме вона порівнює
// гривневий папір із доларовим.
//
// НОМІНАЛЬНА — скільки гривень додасться. Це те, що видно у виписці
// брокера: твердий факт, який ні від чого не залежить.
//
// Меншою реальна є майже завжди, і це не робить її «чеснішою» — просто
// обережнішою. Тому головним числом іде вона (нею порівнюють), а
// номінальна лишається поруч дрібним (нею звіряються).

/** Підпис під числом дохідності: номінальна плюс, за наявності, основа
 *  («до погашення», «дивіденди + зміна ціни»). Порожній рядок, коли
 *  сказати нічого — щоб не малювати порожній рядок під значенням. */
export function yieldNote(nominal, basis = "") {
  const bits = [];
  if (nominal != null) bits.push(`${pct(nominal)} номінальних`);
  if (basis) bits.push(esc(basis));
  return bits.length ? `<div class="sub-xs">${bits.join(" · ")}</div>` : "";
}

/** Пара «реальна головна + номінальна дрібно» одним шматком. */
export function yieldPair(real, nominal, basis = "") {
  return `${pct(real)}${yieldNote(nominal, basis)}`;
}

