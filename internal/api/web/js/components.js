// Дрібні шматки розмітки, спільні для розділів.
//
// Тут живе тільки те, що не залежить ані від даних, ані від контексту, —
// чиста функція «значення -> HTML». Щойно шматку знадобився store чи
// summary, він уже не компонент, а частина розділу.

export function tile(l, v, extra = "") {
  return `<div class="tile"><div class="lbl">${l}</div><div class="val">${v}</div>${extra}</div>`;
}

