-- Чи приймає вклад поповнення.
--
-- Доти помічник реінвесту пропонував докласти в БУДЬ-ЯКИЙ активний вклад,
-- бо розрізнити їх було нічим — і непоповнюваний доводилось ігнорувати
-- очима. Тепер це властивість самого вкладу.
--
-- Типове значення 0: застосунок не вигадує того, чого не знає. Але для
-- наявних рядків одна річ відома напевно — вклад, який УЖЕ поповнювали,
-- поповнюваний за визначенням. Тому одразу після додавання колонки
-- проставляємо прапорець за фактом: доказ сильніший за здогад, і
-- користувачу не доведеться вручну відновлювати те, що видно з даних.
--
-- Решта (вклади без жодного поповнення) лишаються 0, і позначити їх
-- поповнюваними можна перемикачем у рядку вкладу.

ALTER TABLE term_deposits ADD COLUMN replenishable INTEGER NOT NULL DEFAULT 0;

UPDATE term_deposits SET replenishable = 1
WHERE id IN (SELECT DISTINCT deposit_id FROM deposit_topups);
