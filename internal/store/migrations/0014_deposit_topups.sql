-- Поповнення банківських вкладів.
--
-- Поповнюваний вклад (реальний банківський продукт): раз на місяць вклад
-- можна докласти на суму відкриття. Тіло росте, і відсотки нараховуються
-- на більший баланс.
--
-- Поповнення — РУЧНІ записи, а не припущення: кожне реальне докладання це
-- окремий рядок (дата + сума). Так числа чесні — пропущений місяць не
-- роздуває баланс і відсотки. Кожне поповнення дебетує рахунок банку
-- рівно так, як розміщення самого вкладу.
--
-- Адитивна міграція, як 0013: лише CREATE, наявних таблиць не чіпає.
-- amount у мінорних одиницях валюти вкладу (валюта — з term_deposits).

CREATE TABLE deposit_topups (
    id         INTEGER PRIMARY KEY,
    deposit_id INTEGER NOT NULL REFERENCES term_deposits(id),
    date       TEXT    NOT NULL,
    amount     INTEGER NOT NULL
) STRICT;

CREATE INDEX idx_deposit_topups_deposit ON deposit_topups(deposit_id, date);
