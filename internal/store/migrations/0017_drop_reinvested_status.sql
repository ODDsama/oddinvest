-- Позначка «перевкладено» більше не потрібна.
--
-- Вона мала відповідати на питання «чи пішов цей купон у діло». Але
-- купон на 82 ₴ не купує нічого — найдешевший папір коштує тисячу, — тож
-- у покупку він потрапляє змішаним із власними внесками. Питати, який
-- саме купон оплатив конкретний папір, — те саме, що питати, яка саме
-- крапля води в склянці з крана: гроші незлічувані.
--
-- На практиці це означало, що людина клікала навмання заднім числом, і
-- число було правдиве рівно настільки, наскільки сумлінним був клік.
-- Тепер простій рахується сам: купівля з'їдає найстаріший дохід, що
-- надійшов до неї, а зверху число обмежене реальним балансом рахунку.
--
-- «Отримано» лишається — вона не про дисципліну, а про зарахування:
-- нею виплата, датована сьогодні чи наперед, кладеться на рахунок,
-- не чекаючи опівночі. Обидва статуси однаково означали «гроші
-- надійшли», тож зводимо їх до одного й нічого не втрачаємо.
UPDATE payment_status SET status = 'received' WHERE status = 'reinvested';
