-- Одна ціль замість трьох «цілей-сценаріїв».
--
-- Досі було три суми: песимістична / реалістична / оптимістична. Назви
-- обіцяли сценарії, тобто різні майбутні, але лежали всі три на ОДНІЙ
-- кривій прогнозу — темп поповнень і ставка були однакові, відрізнялись
-- лише числа, введені вручну. Наслідки було видно на екрані: менша сума
-- досягалась раніше й тому виглядала «песимістичною», хоча була просто
-- проміжною віхою; а «реалістична» виводилась із прогнозу на дедлайн і
-- через це завжди досягалась рівно в дедлайн — рядок ні про що.
--
-- Тепер сценарії — це допущення (темп і ставка), а ціль одна. Переносимо
-- в неї найамбітнішу з наявних сум, щоб не просити ввести заново. Старі
-- ключі лишаємо на місці: вони більше не читаються, але й не заважають,
-- зате відкат не втратить дані.

INSERT OR REPLACE INTO settings (key, value)
SELECT 'goal_amount_uah', value
FROM settings
WHERE key IN ('goal_optimistic_uah', 'goal_realistic_uah', 'goal_pessimistic_uah')
  AND TRIM(value) <> ''
  AND NOT EXISTS (
      SELECT 1 FROM settings s2
      WHERE s2.key = 'goal_amount_uah' AND TRIM(s2.value) <> ''
  )
ORDER BY CASE key
    WHEN 'goal_optimistic_uah'  THEN 1
    WHEN 'goal_realistic_uah'   THEN 2
    ELSE 3
END
LIMIT 1;
