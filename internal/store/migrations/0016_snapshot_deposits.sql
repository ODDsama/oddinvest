-- Банківські вклади в добовому знімку.
--
-- Та сама діра, що колись була з фондами (0012), і з тієї ж причини:
-- знімок завели, коли третього інструмента ще не існувало. Відтоді вклад
-- став повноцінною частиною портфеля — рахується в капіталі, у драбині,
-- у пасивному доході, — але в історію не потрапляв узагалі. Тому крива
-- «Як росте» показувала капітал без вкладів, а таблиця знімків — теж:
-- сто тисяч на депозиті просто не існували в жодному дні історії.
--
-- Старі рядки лишаються з нулем. Це не спотворення, а факт: у ті дні
-- вклади в знімку ніхто не рахував, і домальовувати їх заднім числом
-- означало б вигадати історію, якої не було. UI зобов'язаний казати
-- «тоді не рахували», а не «тоді не було».
